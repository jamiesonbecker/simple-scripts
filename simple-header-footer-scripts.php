/**
 * Render Credit Author checkbox
 *
 * @since  1.0
 */
function shfs_credit() {
	$options = get_option('shfs_settings');
	$field_value = isset( $options['shfs_credit'] ) ? $options['shfs_credit'] : 0;
	?>
		<input type="checkbox" name="shfs_settings[shfs_credit]" value="1" <?php checked( 1, $field_value ) ?> />
		<p style="width: 300px;" class="description">
			<?php _e( 'Print credit to the author along with your script(s).  Nothing will be publicly visible on your site.', 'shfs' ) ?>
		</p>
	<?php
}

