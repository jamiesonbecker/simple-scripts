/**
 * Print direct link to Simple Header & Footer Scripts admin page
 *
 * Fetches array of links generated by WP Plugin admin page ( Deactivate | Edit )
 * and inserts a link to the Custom CSS admin page
 *
 * @since  1.0
 * @param  array $links Array of links generated by WP in Plugin Admin page.
 * @return array        Array of links to be output on Plugin Admin page.
 */
function shfs_settings_link( $links ) {
	$settings_page = '<a href="' . admin_url('tools.php?page=simple-header-footer-scripts.php' ) .'">Settings</a>';
	array_unshift( $links, $settings_page );
	return $links;
}

$plugin = plugin_basename(__FILE__);

add_filter( "plugin_action_links_$plugin", 'shfs_settings_link' );

/**
 * Register text domain
 *
 * @since 1.0
 */
function shfs_textdomain() {
	load_plugin_textdomain('shfs');
}

add_action('init', 'shfs_textdomain');


/**
 * Delete Settings on Uninstall
 *
 * @since 1.0
 */
function shfs_uninstall() {
	unregister_setting( 'shfs_settings_group', 'shfs_settings' );
}

register_uninstall_hook( __FILE__, 'shfs_uninstall' );

/**
 * Print Header Scripts
 *
 * @since 1.0
 */
function shfs_header_scripts() {
	ob_start();
		$options = get_option( 'shfs_settings' );
		$sfhs_header = isset( $options['header_scripts_input'] ) ? $options['header_scripts_input'] : '';
		echo "<script type=text/javascript>";
		if ( isset( $options['shfs_credit'] ) ) {
			echo "/*
 * Rendered by the Simple Header & Footer Scripts Plugin
 * http://wordpress.org/plugins/simple-header-footer-scripts/
 */\n";
		}
		echo $sfhs_header;
		echo "</script>";
	echo ob_get_clean();
}

/**
 * Print Footer Scripts
 *
 * @since 1.0
 */
function shfs_footer_scripts() {
	ob_start();
		$options = get_option( 'shfs_settings' );
		$sfhs_footer = isset( $options['footer_scripts_input'] ) ? $options['footer_scripts_input'] : '';
		echo "<script type=text/javascript>";
		if ( isset( $options['shfs_credit'] ) ) {
			echo "/*
 * Rendered by the Simple Header & Footer Scripts Plugin
 * http://wordpress.org/plugins/simple-header-footer-scripts/
 */\n";
		}
		echo $sfhs_footer;
		echo "</script>";
	echo ob_get_clean();
}

/**
 * Attaches Scripts to Hooks to be rendered
 *
 * @since 1.0
 */
function shfs_render_scripts() {
	$options = get_option( 'shfs_settings' );
	if ( isset( $options['header_scripts_input'] ) )
		add_action( 'wp_head', 'shfs_header_scripts' );

	if ( isset( $options['footer_scripts_input'] ) )
		add_action( 'wp_footer', 'shfs_footer_scripts' );
}

add_action( 'init', 'shfs_render_scripts' );

/**
 * Register "Header/Footer Scripts" submenu in "Tools" Admin Menu
 *
 * @since 1.0
 */
function shfs_register_submenu_page() {
	add_management_page( __( 'Simple Header & Footer Scripts', 'shfs' ), __( 'Header/Footer Scripts', 'shfs' ), 'edit_themes', basename(__FILE__), 'shfs_render_submenu_page' );
}

add_action( 'admin_menu', 'shfs_register_submenu_page' );


/**
 * Register settings
 *
 * @since 1.0
 */
function shfs_register_settings() {
	register_setting( 'shfs_settings_group', 'shfs_settings' );

	add_settings_section( 'shfs_primary_section', __( '', 'rt_polls' ), 'shfs_primary_section_cb', __FILE__ );

	add_settings_field( 'shfs_header_scripts_input', __( 'Header Scripts', 'rt_polls' ), 'shfs_header_scripts_input', __FILE__, 'shfs_primary_section' );

	add_settings_field( 'shfs_footer_scripts_input', __( 'Footer Scripts', 'rt_polls' ), 'shfs_footer_scripts_input', __FILE__, 'shfs_primary_section' );

	add_settings_field( 'shfs_credit', __( 'This Plugin is Really Helpful!', 'rt_polls' ), 'shfs_credit', __FILE__, 'shfs_primary_section' );
}

add_action('admin_init', 'shfs_register_settings');

